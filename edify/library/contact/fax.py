"""``fax`` — fax-number shape (same permissive form as phone)."""

from __future__ import annotations

from edify import Pattern

fax = (
    Pattern()
    .start_of_input()
    .optional()
    .char("+")
    .between_lazy(1, 4)
    .digit()
    .optional()
    .any_of()
    .char("-")
    .char(".")
    .whitespace_char()
    .end()
    .optional()
    .char("(")
    .between_lazy(1, 3)
    .digit()
    .optional()
    .char(")")
    .optional()
    .any_of()
    .char("-")
    .char(".")
    .whitespace_char()
    .end()
    .between(1, 4)
    .digit()
    .optional()
    .any_of()
    .char("-")
    .char(".")
    .whitespace_char()
    .end()
    .between(1, 4)
    .digit()
    .optional()
    .any_of()
    .char("-")
    .char(".")
    .whitespace_char()
    .end()
    .between(1, 9)
    .digit()
    .end_of_input()
)
"""Callable :class:`Pattern` for the permissive international fax-number shape."""
